export function getTermBySlug(slug: string): HhggTerm | undefined {
  return hhggTerms.find((t) => t.slug === slug);
}

export function searchTerms(query: string): HhggTerm[] {
  if (!query) return hhggTerms;
  const q = query.toLowerCase();
  return hhggTerms.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q)
  );
}
